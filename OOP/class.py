class Bottle:
    brand = "Hawa"
    liter = 1
    price = 100

my_bottle = Bottle()

print(my_bottle.brand)
print(my_bottle.liter)
print(my_bottle.price)

# Output:
# Hawa
# 1
# 100

your_bottle = Bottle()

your_bottle.brand = "Pepsi"
your_bottle.liter = 2
your_bottle.price = 200

print(your_bottle.brand)
print(your_bottle.liter)
print(your_bottle.price)

# Output:
# Pepsi
# 2
# 200
